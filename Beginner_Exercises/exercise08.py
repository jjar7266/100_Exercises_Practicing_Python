"""
Write a Python program that displays the numbers from 1 to 20
inclusive on the console.

Note: You need to create two versions, one with the for loop
      and the  other with the while loop.
"""

# Write your code here :-)

for i in range(1, 21):
    print(i)

num = 1

while num <= 20:
    print(num)
    num += 1
    
