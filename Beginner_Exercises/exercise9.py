"""
Write a Python program that displays only the odd numbers between 
10 and 20 inclusive.

Note: You need to create two versions, one with the for loop 
      and the other with the while loop.
"""

# Write your code here :-)

for i in range(10, 21):
    if i % 2 != 0:
        print(i)


num = 10

while num <= 20:
    if num % 2 != 0:
        print(num)

    num += 1     

    




    



    